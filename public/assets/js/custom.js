/*
    This MultiCheck Function is recommanded for datatable
*/

function multiCheck(tb_var, closest = "table") {
    tb_var.on("change", ".chk-parent", function() {
        var e = $(this).closest(closest).find("td:first-child .child-chk"),
            r = $(this).closest(closest).find(".chk-parent"),
            a = $(this).is(":checked");
        $(r).each(function () {
            a ? $(this).prop("checked", !0) : $(this).prop("checked", !1);
        });
        $(e).each(function () {
            a ? ($(this).prop("checked", !0).change(), $(this).closest("tr").addClass("active")) : ($(this).prop("checked", !1).change(), $(this).closest("tr").removeClass("active"));
        });
    }),
    tb_var.on("change", "tbody tr .new-control", function() {
        $(this).parents("tr").toggleClass("active")
    })
}

